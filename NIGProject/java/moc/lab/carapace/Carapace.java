package moc.lab.carapace;

public class Carapace {
	
	private int Px;
	private int Py;
	
	public Carapace(int Px,int Py){
		this.Px = Px;
		this.Py = Py;
	}

	public int getPx() {
		return Px;
	}

	public void setPx(int px) {
		Px = px;
	}

	public int getPy() {
		return Py;
	}
	
	public void setPy(int py) {
		Py = py;
	}

}
